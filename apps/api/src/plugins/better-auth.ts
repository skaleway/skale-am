import { Elysia } from "elysia";
import { auth } from "@skaleam/auth/auth";

/**
 * Better Auth plugin for Elysia
 * - Mounts the auth handler at /api/auth/*
 * - Provides a macro for protected routes that injects user and session
 */
export const betterAuthPlugin = new Elysia({ name: "better-auth" })
  .mount("/api/auth", auth.handler)
  .macro({
    auth: {
      async resolve({ status, request: { headers } }) {
        const session = await auth.api.getSession({
          headers,
        });

        if (!session) {
          return status(401);
        }

        return {
          user: session.user,
          session: session.session,
        };
      },
    },
  });

export type BetterAuthPlugin = typeof betterAuthPlugin;
